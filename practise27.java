
public class practise27 {

    public static void main(String[] args) {
        int num = 8;
        int sum = 0;
        for (int i = 1; i <= 10; i++) {
            int value = i * num;
            // System.out.println(value);
            sum = sum + value;
        }
        System.out.println(sum);
    }
}
