// escape sequence

public class program8 {
    public static void main(String[] args) {
        System.out.println("My name is \t Sharmila");
        System.out.println("My name is \' Sharmila \'");
        System.out.println("My name is \" Sharmila \"");
        System.out.println("My name is \r Sharmila");
        System.out.println("My name is \\ Sharmila");
        System.out.println("My name is \n Sharmila");
        System.out.println("My name is \f Sharmila");
        System.out.println("My name is \b Sharmila");
        System.out.println("My name is Sharmila");
    }
}
